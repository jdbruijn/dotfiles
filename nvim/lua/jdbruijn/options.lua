-- Options, see https://neovim.io/doc/user/options.html for details.

-- Highlight the line of the cursor.
vim.opt.cursorline = true

-- Ignore case in search patterns unless "\C" or uppercase search terms.
vim.opt.ignorecase = true
vim.opt.smartcase = true

-- Disable mouse support.
vim.opt.mouse = ''

-- Show relative numbers, but include the absolute number on the current line.
vim.opt.number = true
vim.opt.relativenumber = true

-- Minimal number of screen lines to keep above and below the cursor.
vim.opt.scrolloff = 10

-- Always show the sign column, https://neovim.io/doc/user/sign.html.
vim.opt.signcolumn = 'yes'
